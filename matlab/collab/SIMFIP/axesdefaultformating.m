function axesdefaultformating( ax )
% AXESDEFAULTFORMATING Set some default formating to axes
%   axesdefaultformating( ax )
%   ax : ax handle
set(ax,'XMinorTick','on')
set(ax,'YMinorTick','on')
set(ax,'Box','on')
set(ax,'FontSize',12)
set(ax,'Layer','top')
set(ax,'XColor','k')
set(ax,'YColor','k')
set(ax,'ZColor','k')
end

